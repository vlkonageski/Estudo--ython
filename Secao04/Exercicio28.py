"""
Faça a leitura de três valores e apresente como resultado a soma dos quadrados dos três valores lidos.
"""

n1 = int(input("Informe o primeiro valor:"))
n2 = int(input("Informe o segundo valor:"))
n3 = int(input("Informe o terceiro valor:"))

soma = (n1 * n1) + (n2 * n2) + (n3 * n3)

print("A soma dos quadrado dos trÊs valores é:", soma)
